# Example: Pattern printing with nested for loops
rows = 5

# Upper half of the pattern
for i in range(1, rows + 1):
    for j in range(1, i + 1):
        print("*", end=" ")
    print()

# Lower half of the pattern
for i in range(rows - 1, 0, -1):
    for j in range(1, i + 1):
        print("*", end=" ")
    print()




















